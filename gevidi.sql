
USE gevidi;

CREATE TABLE usuario (
    id INT AUTO_INCREMENT PRIMARY KEY,
    rpe VARCHAR(20) NOT NULL UNIQUE,
    nombre VARCHAR(100) NOT NULL,
    apellidos VARCHAR(100) NOT NULL,
    contrasenia VARCHAR(100) NOT NULL
);

CREATE TABLE rol (
	id int AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE usuarioRol (
	usuarioId INT NOT NULL,
    rolId INT NOT NULL,
    PRIMARY KEY (usuarioId, rolId),
    FOREIGN KEY (usuarioId) REFERENCES usuario(id),
    FOREIGN KEY (rolId) REFERENCES rol(id)
);

CREATE TABLE centroTrabajo (
	id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE proceso (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE vehiculo (
	id INT AUTO_INCREMENT PRIMARY KEY,
    economico VARCHAR(20) UNIQUE NOT NULL,
    placa VARCHAR(10) UNIQUE NOT NULL,
	propiedad ENUM('PROPIO', 'ARRENDADO') NOT NULL,
    kilometraje INT NOT NULL, 
    marca VARCHAR(30) NOT NULL,
    modelo VARCHAR(30) NOT NULL,
    anio YEAR NOT NULL,
    centroTrabajoId INT NOT NULL,
    procesoId INT NOT NULL,
    estado ENUM('DISPONIBLE', 'OPERANDO_CON_FALLA', 'INDISPONIBLE') NOT NULL,
    FOREIGN KEY (centroTrabajoId) REFERENCES centroTrabajo(id),
    FOREIGN KEY (procesoId) REFERENCES proceso(id)
);

CREATE TABLE tipoFalla (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE reporteVehiculo (
    id INT AUTO_INCREMENT PRIMARY KEY,
    vehiculoId INT NOT NULL,
    usuarioId INT NOT NULL,
    nuevoEstado ENUM('DISPONIBLE', 'OPERANDO_CON_FALLA', 'INDISPONIBLE') NOT NULL,
    tipoFallaId INT NULL,
    fallaPersonalizada VARCHAR(100),
    kilometraje INT NULL,
    ubicacionIndisponible ENUM('TALLER', 'PATIO') DEFAULT NULL,
    fechaReporte DATETIME DEFAULT CURRENT_TIMESTAMP,
    tiempoTranscurrido BIGINT NULL,
    FOREIGN KEY (vehiculoId) REFERENCES vehiculo(id),
    FOREIGN KEY (usuarioId) REFERENCES usuario(id),
    FOREIGN KEY (tipoFallaId) REFERENCES tipoFalla(id)

);

ALTER TABLE reporteVehiculo DROP FOREIGN KEY reporteVehiculo_ibfk_1;

ALTER TABLE reporteVehiculo
  ADD CONSTRAINT reporteVehiculo_ibfk_1
  FOREIGN KEY (vehiculoId)
  REFERENCES vehiculo(id)
  ON DELETE CASCADE
  ON UPDATE RESTRICT;

-- TPS Y LECTORES
CREATE TABLE dispositivo (
  id INT AUTO_INCREMENT PRIMARY KEY,
  numeroSerie VARCHAR(50) NOT NULL UNIQUE,
  estado ENUM('ACTIVO', 'DEFECTUOSO') NOT NULL DEFAULT 'ACTIVO',
  centroTrabajoId INT NOT NULL,
  tipoDispositivo ENUM('TP_NEWLAND','LECTOR_NEWLAND','TP_DOLPHIN_9900','LECTOR_DOLPHIN_9900', 'BLUEBIRD', 'CELULAR_OTROS') NOT NULL,
  FOREIGN KEY (centroTrabajoId) REFERENCES centroTrabajo(id)
);

CREATE TABLE tipoFallaDispositivo (
  id INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE reporteDispositivo (
  id INT AUTO_INCREMENT PRIMARY KEY,
  dispositivoId INT NOT NULL,
  tipoDispositivo ENUM('TP_NEWLAND','LECTOR_NEWLAND','TP_DOLPHIN_9900','LECTOR_DOLPHIN_9900', 'BLUEBIRD', 'CELULAR_OTROS') NOT NULL,
  centroTrabajoId INT NOT NULL,
  tipoFallaDispositivoId INT,  -- Puede ser nulo si se llena falla_personalizada
  fallapersonalizada VARCHAR(100),
  fechaReporte DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (dispositivoId) REFERENCES dispositivo(id),
  FOREIGN KEY (centroTrabajoId) REFERENCES centroTrabajo(id),
  FOREIGN KEY (tipoFallaDispositivoId) REFERENCES tipoFallaDispositivo(id)
);

ALTER TABLE reporteDispositivo DROP FOREIGN KEY reporteDispositivo_ibfk_1;

ALTER TABLE reporteDispositivo
  ADD CONSTRAINT reporteDispositivo_ibfk_1
  FOREIGN KEY (dispositivoId)
  REFERENCES dispositivo(id)
  ON DELETE CASCADE
  ON UPDATE RESTRICT;
  
